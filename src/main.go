func findBall(grid [][]int) []int {
	const NO = -1
	rowCount := len(grid)
	colCount := len(grid[0])
	result := make([]int, colCount)

	for col := 0; col < colCount; col += 1 {
		currentCol := col
		for row := 0; row < rowCount; row += 1 {
			current := grid[row][currentCol]
			nextColumn := currentCol + current

			if (nextColumn < 0) || (nextColumn >= colCount) || (current != grid[row][nextColumn]) {
				result[col] = NO
				break
			} else {
				result[col] = nextColumn
				currentCol = nextColumn
			}
		}
	}

	return result
}

