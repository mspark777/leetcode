from __future__ import annotations
from typing import Optional, List


class Solution:
    def findBall(self, grid: List[List[int]]) -> List[int]:
        NO = -1
        row_count = len(grid)
        col_count = len(grid[0])
        result = [0] * col_count

        for col in range(col_count):
            current_col = col
            for row in range(row_count):
                current = grid[row][current_col]
                next_column = current_col + current

                if (
                    (next_column < 0)
                    or (next_column > (col_count - 1))
                    or (current != grid[row][next_column])
                ):
                    result[col] = NO
                    break
                else:
                    result[col] = next_column
                    current_col = next_column

        return result


